		<h2><strong>Описание сварных полиэтиленовых фитингов</strong></h2>
		<p><strong>Сварной (сегментный) фитинг</strong> – это соединительная часть трубо- провода, устанавливаемая в местах его разветвлений, поворотов, переходов на другой диаметр, присоединения трубопроводной арматуры и т.п.</p>
		<h2><strong>Производство сварных фитингов</strong></h2>
		<p>Сварные фитинги изготавливаются методом сварки встык из от- резков полиэтиленовых труб в соответствии с оригинальными техническими условиями ТУ 2248-004-12981894-2013 Трубпласт. Производственные мощности нашего предприятия позволяют охватить практически весь спектр диаметров полиэтиленовых труб и осуществлять на их основе производство сварных фитин- гов как стандартных, так и нестандартных типов. Гарантия качества наших изделий это, прежде всего высокопро- фессиональная подготовка персонала, использование высоко- технологичного сварочного оборудования и контроль качества продукции.</p>
		<h3><strong>Применение сварных фитингов</strong></h3>
		<p>Сварные фитинги ПЭ используются при монтаже напорных тру- бопроводов, труб для наружной канализации в местах поворотов, разветвлений, изменений диаметра трассы, присоединения тру- бопроводной арматуры и т. п. Рабочая среда: вода, (при темпе- ратуре от 0 ºС до 40 ºС), неагрессивные жидкие и газообразные вещества, к которым полиэтилен химически стоек (за исключе- нием горючих газов).</p>
		<p><img alt="Фитинги сварные сегментные" height="217" src="<?=SITE_TEMPLATE_PATH; ?>/images/fitingi/fitingi-svarnie-segmentnie.jpg" title="Фитинги сварные сегментные" width="215" /><img alt="Фитинги сварные сегментные" height="217" src="<?=SITE_TEMPLATE_PATH; ?>/images/fitingi/fitingi-svarnie-segmentnie-2.jpg" title="Фитинги сварные сегментные" width="216" /></p>
		<p>Изготавливаются в заводских условиях по ТУ2248-005-59355492-2005 или эскизу заказчика методом стыковой сварки заготовок полиэтиленовых труб ПЭ 80 и ПЭ 100. Сварка ПЭ труб методом сварки встык — современный и малозатратный способ конструирования фасонных частей трубопроводов практически любой сложности. Сварные фитинги используются при монтаже напорных трубопроводов (по ГОСТ 18599-2001), труб для наружной напорной канализации в местах поворотов, разветвлений, изменений диаметра трассы, присоединения трубопроводной арматуры и т. п.</p>
		<p>После монтажа сегментные фитинги должны быть замоноличены в армированный бетон (в виде опор) для снятия дополнительных нагрузок (изгибающих, осевых), возникающих под действием внутреннего давления в местах поворотов и ответвлений трубопроводов. Конструкция и размеры опор зависят от максимального рабочего давления трубопровода и свойств грунта. Опоры из бетона не должны создавать дополнительную нагрузку на трубопровод и должны устанавливаться на твердый и тщательно уплотненный грунт. Ориентировочные размеры и конструкция опор приведены в ТУ 2248—005—59355492—2005.</p>
		<p>Кроме того, необходимо учитывать что трубопроводы из полимерных материалов подвержены значительному изменению линейных размеров под воздействием температуры ввиду большого значения коэффициента теплового линейного расширения полиэтилена по сравнению с другими материалами. Для того, чтобы уменьшить температурную деформацию, следует использовать рациональную схему расстановки неподвижных опор, делящих трубопровод на участки, температурная деформация которых происходит независимо один от другого.</p>
		<p>Неподвижная опора может быть изготовлена в заводских условиях и приобретена как готовое сварное изделие. Другой вариант — использование электросварных пластин, которые позволяют приварить сегменты труб к необходимому участку трубопровода непосредственно на объекте.</p>
		<p><strong>Типичные места размещения неподвижных опор:</strong></p>
		<ul>
			<li>перед входами в камеры (колодцы) и после выхода из них;</li>
			<li>перед отводами и тройниками, а также после них</li>
			<li>для минимизации дополнительных нагрузок (изгибающих, осевых), возникающих под действием внутреннего давления в местах поворотов и ответвлений трубопроводов.</li>
		</ul>
		<p>Неподвижные опоры должны быть закреплены в армированном бетоне, причем фиксирующие их бетонные конструкции должны устанавливаться на твердый и тщательно уплотненный грунт, не создавать дополнительную нагрузку на трубопровод, а главное — обеспечивать сопротивление соответствующему расчетному усилию.</p>
